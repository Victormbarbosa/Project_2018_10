    void DFS(Vertice vi) throws InterruptedException {
        vi.setEstado(true);

        for (Vertice vk : vi.getVerticesad()) {
            if (vk.isEstado() == false) {
                Thread.sleep(1000);
                dibujarAristas(vk, vi);
                DFS(vk);
            }
        }
    }

